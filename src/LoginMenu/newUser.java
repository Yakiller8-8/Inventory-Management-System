    private void signUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpBtnActionPerformed
        // TODO add your handling code here:
        String username = nuser.getText();
        String password = npass.getText();
        String repassword = nrpass.getText();
        String email = nemail.getText()+"@gmail.com";
        
        if(username.equals("")&&password.equals("")&&repassword.equals("")&&email.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty\nPassword field is empty\nRe-enter password field is empty\nEmail field is empty");
            
        } else if(username.equals("")&&password.equals("")&&repassword.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty\nPassword field is empty\nRe-enter password field is empty");
            
        } else if(username.equals("")&&repassword.equals("")&&email.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty\nRe-enter password field is empty\nEmail field is empty");
           
        } else if(username.equals("")&&email.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty\nEmail field is empty");
            
        } else if(username.equals("")&&password.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty\nPassword field is empty");
                       
        } else if(username.equals("")&&repassword.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty\nRe-enter password field is empty");
           
        } else if(username.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty");
            
        } else if(password.equals("")&&repassword.equals("")&&email.equals("")){
            JOptionPane.showMessageDialog(null, "Password field is empty\nRe-enter password field is empty\nEmail field is empty");
            
        } else if(repassword.equals("")&&email.equals("")){
            JOptionPane.showMessageDialog(null, "Re-enter password field is empty\nEmail field is empty");
            
        } else if(password.equals("")&&email.equals("")){
            JOptionPane.showMessageDialog(null, "Password field is empty\nEmail field is empty");
            
        } else if(email.equals("")){
            JOptionPane.showMessageDialog(null, "Email field is empty");
            
        } else if(username.equals("")&&password.equals("")&&email.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is empty\nPassword field is empty\nEmail field is empty");
            
        } else if(password.equals("")&&repassword.equals("")){
            JOptionPane.showMessageDialog(null, "Password field is empty\nRe-enter password field is empty");
            
        } else if(!password.equals(repassword)){
            JOptionPane.showMessageDialog(null, "Re-enter your password");
            nrpass.setText("");
            
        } else {
        PreparedStatement pst;
        String query = "INSERT INTO `InventoryManagementSystem`.`signlog`(`username`,`password`,`email`) VALUES (?,?,?);";
        try {
            pst = dbConnection.getConnection().prepareStatement(query);
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, email);
            
            if(pst.executeUpdate() >  0){
                JOptionPane.showMessageDialog(null,"New user added");
                Login l = new Login();
                l.setVisible(true);
                setVisible(false);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(newUser.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_signUpBtnActionPerformed

