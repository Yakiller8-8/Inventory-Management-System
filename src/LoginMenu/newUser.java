    private void revealAsteriskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_revealAsteriskActionPerformed
        // TODO add your handling code here:
        if(revealAsterisk.isSelected()){
            // Show the password in plain text
            npass.setEchoChar((char)0);
            nrpass.setEchoChar((char)0);
        }else {
            // Hide the password behind asterisks
            npass.setEchoChar('*');
            nrpass.setEchoChar('*');
        }
    }//GEN-LAST:event_revealAsteriskActionPerformed

