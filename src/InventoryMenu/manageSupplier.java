    private void deletebtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deletebtnMouseClicked
        // TODO add your handling code here:
        if(sname.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter the supllier to be deleted");
        } else{
            try{
                String id = supplierid.getText();
                String query2 = "DELETE from InventoryManagementSystem.supplier where `Supplier ID` ="+id;
                Statement add = dbConnection.getConnection().createStatement();
                add.executeUpdate(query2);
                
                JOptionPane.showMessageDialog(this, "Supplier Successfully Deleted");
                
                String query = "SELECT `Supplier ID` FROM supplier WHERE `Supplier ID` > ?";
                PreparedStatement stmt = dbConnection.getConnection().prepareStatement(query);
                stmt.setInt(1, Integer.valueOf(id));
                ResultSet results = stmt.executeQuery();

                while (results.next()) {
                    int currentID = results.getInt(1);
                    int newID = currentID - 1;

                    // Update the corresponding row in the database
                    query = "UPDATE supplier SET `Supplier ID` = ? WHERE `Supplier ID` = ?";
                    PreparedStatement updateStmt = dbConnection.getConnection().prepareStatement(query);
                    updateStmt.setInt(1, newID);
                    updateStmt.setInt(2, currentID);
                    updateStmt.executeUpdate();
                }
                SelectSupplier();
                supplierid.setText("");
                sname.setText("");
                phone.setText("");
            } catch(SQLException e){
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_deletebtnMouseClicked

