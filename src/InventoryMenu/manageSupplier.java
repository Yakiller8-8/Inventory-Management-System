    public void SelectSupplier(){
        try{
            st = dbConnection.getConnection().createStatement();
            String reset = "ALTER TABLE InventoryManagementSystem.supplier AUTO_INCREMENT = 1 ";
            PreparedStatement r  = dbConnection.getConnection().prepareStatement(reset);
            r.executeUpdate(); 
            rs = st.executeQuery("SELECT * FROM InventoryManagementSystem.supplier");
            supplierTable.setModel(DbUtils.resultSetToTableModel(rs));
        } catch(Exception e){
                e.printStackTrace();
        }
    }
    public void getCategory(){
        try{
            st = dbConnection.getConnection().createStatement();
            String query = "SELECT * FROM InventoryManagementSystem.category";
            rs = st.executeQuery(query);
            while(rs.next()){
                String myCat = rs.getString("Category Name");
                categorycb.addItem(myCat);
            }
        } catch(Exception e){
                e.printStackTrace();
        }
    }
    
